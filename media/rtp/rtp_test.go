func TestRemoveRecoverPadding(t *testing.T) {
	tests := []struct {
		name            string
		pkt             []byte
		wantStrippedLen int
	}{
		{"2 padding packets", rtpWithPadding, 1328 - 2*184},
		{"7 padding packets", rtpPaddingOnly, 12 + 7*4},
	}

	for _, test := range tests {
		t.Run(test.name, func(t *testing.T) {
			orgLen := len(test.pkt)
			pkt := make([]byte, 0, 3000)
			pkt = append(pkt, test.pkt...)

			noPadding, err := RemoveTsPadding(pkt)
			require.NoError(t, err)

			require.Less(t, len(noPadding), orgLen)
			log.Info("removed padding", "with_padding", orgLen, "no_padding", len(noPadding))

			recovered, err := RecoverTsPadding(pkt, len(noPadding))
			require.NoError(t, err)

			require.Equal(t, orgLen, len(recovered))
			log.Info("recovered padding", "with_padding", orgLen, "recovered", len(recovered))

			for i := 0; i < orgLen; i++ {
				require.Equal(t, test.pkt[i], recovered[i], "pos=%d", i)
			}

			require.Equal(t, test.pkt, recovered)
		})
	}

}
